public class ContainsDuplicateSolution
{
    // Method to check if an array contains any duplicates
    public bool hasDuplicate(int[] nums) {
        var seen = new HashSet<int>();
        foreach (int num in nums) {
            if (seen.Contains(num)) {
                return true;
            }
            seen.Add(num);
        }
        return false;
    }
}
